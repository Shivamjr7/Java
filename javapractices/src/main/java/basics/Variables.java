package basics;

public class Variables {


    public static void main(String[] args) {

        /**
         * Signed : both +ve and -ve using 2's complement
         * Unsigned : only +ve values 
         */
        byte _byte = 127; // stores 8 bits (from -128 to 127) signed numbers
        int _int = 1; //  4 bytes (32 bits)


        float _float = 1.1f; // 4 bytes

        long _long = 123; // 8 bytes (64 bits)
        double _double = 1.2; // 8 bytes (64 bits)
    }

}
